char	*ft_strrchr(const char *s, int c)
{
	char	x;
	char	*ptr;

	x = (char)c;
	ptr = 0;
	while (*s)
	{
		if (*s == x)
			ptr = (char *)s;
		s++;
	}
	if (*s == x)
		return ((char *)s);
	return (ptr);
}
