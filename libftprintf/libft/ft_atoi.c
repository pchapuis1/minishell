int	ft_atoi(const char *s)
{
	int	sign;
	int	sum;

	sign = 1;
	sum = 0;
	while (*s == ' ' || *s == '\f' || *s == '\r'
		|| *s == '\t' || *s == '\v' || *s == '\n')
		s++;
	if (*s == '+')
		s++;
	else if (*s == '-')
	{
		sign = -1;
		s++;
	}
	while ('0' <= *s && *s <= '9')
	{
		sum = sum * 10 + *s - '0';
		s++;
	}
	return (sum * sign);
}
